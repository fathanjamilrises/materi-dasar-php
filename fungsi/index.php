<?php 

// Sebuah Fungsi Menghitung Hasil Kali Yang Mengambil Parameter Dari Variabel $a dan $b
function hasilKali ($a,$b) 
{
    $hasil = $a * $b;
    echo "Hasil Perkalian Dari $a dan $b adalah $hasil";
}

hasilKali(5,9); // Menjalankan Fungsi Dengan Mengisi Nilai Dari Parameter Variabel $a dan $b



?>